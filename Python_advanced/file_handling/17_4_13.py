with open('output.txt', 'a', encoding='utf-8') as smt:
    for i in range(int(input())):
        with open(input(), encoding='utf-8') as file:
            smt.write(file.read() + '\n')

# The input to the program is a natural number n and n lines with file names.
# The program creates the output.txt file and displays the contents of all files
# with the specified names without changing their order.