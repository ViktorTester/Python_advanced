import turtle


turtle.shape('circle')
turtle.color("blue", "green")
turtle.pensize(5)
for _ in range(2):
    turtle.forward(150)
    turtle.stamp()
    turtle.left(90)
    turtle.forward(75)
    turtle.stamp()
    turtle.left(90)



turtle.exitonclick()

